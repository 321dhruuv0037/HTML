let container = document.querySelector(".container");
let winScreen = document.querySelector(".win-screen");
let submitButton = document.querySelector(".submit");
let inputCount, tryCount, inputRow;
let backSpaceCount = 0;
let randomWord, finalWord;

const isTouchDevice = () => {
    try {
        document.createEvent("TouchEvent");
        return true;
    } catch (e) {
        return false;
    }
};

//Initial Setup
const startGame = async () => {
    winScreen.classList.add("hide");
    container.innerHTML = "";
    inputCount = 0;
    successCount = 0;
    tryCount = 0;
    finalWord = "";

    //Creating the grid
    for (let i = 0; i < 6; i++) {
        let inputGroup = document.createElement("div");
        inputGroup.classList.add("input-group");
        for (let j = 0; j < 5; j++) {
            //Disabled all boxes by default
            inputGroup.innerHTML += `<input type="text" class="input-box" onkeyup="checker(event)" maxlength="1" disabled>`;
        }
        await container.appendChild(inputGroup);
    }
    inputRow = document.querySelectorAll(".input-group");
    inputBox = document.querySelectorAll(".input-box");
    updateDivConfig(inputRow[tryCount].firstChild, false);
    randomWord = getRandom();
    console.log(randomWord);
};

//Gets random word
const getRandom = () => words[Math.floor(Math.random() * words.length)].toUpperCase();

const updateDivConfig = (element, disabledStatus) => {
    element.disabled = disabledStatus;
    if (!disabledStatus) {
        element.focus();
    }
};

const checker = async (e) => {
    let value = e.target.value.toUpperCase();
    //disables current input box
    updateDivConfig(e.target, true);
    if (value.length == 1) {
        if (inputCount <= 4 && e.key != "Backspace") {
            //Attach the letter to the final word
            finalWord += value;
            if (inputCount < 4) {
                //enable next box
                updateDivConfig(e.target.nextSibling, false);
            }
        }
        inputCount += 1;
    } else if (value.length == 0 && e.key == "Backspace") {
        finalWord = finalWord.substring(0, finalWord.length - 1);
        if (inputCount == 0) {
            updateDivConfig(e.target, false);
            return false;
        }
        updateDivConfig(e.target, true);
        e.target.previousSibling.value = "";
        updateDivConfig(e.target.previousSibling, false);
        inputCount = -1;
    }
};

window.addEventListener("keyup", (e) => {
    if (inputCount > 4) {
        if (isTouchDevice()) {
            submitButton.classList.remove("hide");
        }
        if (e.key == "Enter") {
            validateWord();
        } else if (e.key == "Backspace") {
            inputRow[tryCount].lastChild.value = "";
            finalWord = finalWord.substring(0, finalWord.length - 1);
            updateDivConfig(inputRow[tryCount].lastChild, false);
            inputCount -= 1;
        }
    }
});

//Comparison Logic
const validateWord = async () => {
    if (isTouchDevice()) {
        submitButton.classList.add("hide");
    }
    let failed = false;
    let currentInputs = inputRow[tryCount].querySelectorAll(".input-box");
    //Check if input is a valid English word
    await fetch(
        `https://api.dictionaryapi.dev/api/v2/entries/en/${finalWord}`
    ).then((response) => {
        if (response.status == "404") {
            console.clear();
            alert("Please Enter Valid Word");
            failed = true;
        }
    });
    if (failed) {
        return false;
    }
    let successCount = 0;
    let successLetters = "";
    //Compares both words
    for (let i in randomWord) {
        if (finalWord[i] == randomWord[i]) { //alphabet is guessed at correct position
            currentInputs[i].classList.add("correct");
            successCount += 1;
            successLetters += randomWord[i];
        } else if (randomWord.includes(finalWord[i]) && !successLetters.includes(finalWord[i])) {
            //alphabet is present in word but is at wrong position
            currentInputs[i].classList.add("exists");
        } else {
            currentInputs[i].classList.add("incorrect");
        }
    }
    tryCount += 1;
    //If word is guessed
    if (successCount == 5) {
        //Displays the win banner
        setTimeout(() => {
            winScreen.classList.remove("hide");
            winScreen.innerHTML = ` <span>Total guesses: ${tryCount}</span>
                                    <button onclick="startGame()">New Game</button>`;
        }, 1000);
    } else {
        //Unsuccessful attempt
        inputCount = 0;
        finalWord = "";
        if (tryCount == 6) {
            //All attempts exhausted
            tryCount = 0;
            winScreen.classList.remove("hide");
            winScreen.innerHTML = ` <span>You lose</span>
                                    <button onclick="startGame()">New Game</button>`;
            return false;
        }
        updateDivConfig(inputRow[tryCount].firstChild, false);
    }
    inputCount = 0;
};

window.onload = startGame();